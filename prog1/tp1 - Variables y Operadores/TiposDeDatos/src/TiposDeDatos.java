public class TiposDeDatos {
    public static void main(String[]args) {
        int entero;
        double valorConComa;
        char caracter;
        boolean valorLogico;

        entero = 21;
        valorConComa = 21.56;
        caracter = 'a';
        valorLogico = true;

        // Muestro los valores de las variables
        System.out.println("Valor entero: " + entero);
        System.out.println("Valor con coma: " + valorConComa);
        System.out.println("Valor char: " + caracter);
        System.out.println("Valor logico: " + valorLogico);
    }
}
